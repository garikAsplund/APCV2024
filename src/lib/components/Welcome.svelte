	<h3 class="h3 text-center">The Asia Pacific Conference on Vision is returning!</h3>
	<hr class="h-0.5 bg-surface-800-100-token" />
	<p>Dear vision science community,</p>
	<p>
		We are excited that APCV will be returning after a five-year hiatus. The aim of the conference
		is to facilitate discussion on vision research in Asia and the Pacific. Participants from all
		over the world are welcome.
	</p>
	<p>
		Vision research centers on sight, on how humans and others transform light into perception,
		thought, and action. By its nature, vision research is widely interdisciplinary, involving
		aspects of psychology, neuroscience, computer science, engineering, medicine, philosophy, and
		visual art.
	</p>

	<p>Conference topics will include, but are not limited to:</p>
	<ul class="mx-8 space-y-1 text-base">
		<li>Visual psychophysics</li>
		<li>Visual neuroscience</li>
		<li>Visual cognition</li>
		<li>Computer vision</li>
		<li>Multisensory integration</li>
		<li>Decisions and action</li>
	</ul>
	<p>
		The conference program will consist of keynote lectures, symposia, talks, and posters. We invite
		original works on all aspects of vision science research.
	</p>
	<p>We look forward to seeing you in Singapore!</p>
	<p>Sincerely,</p>
	<p>The APCV 2024 organizing committee</p>
</div>

